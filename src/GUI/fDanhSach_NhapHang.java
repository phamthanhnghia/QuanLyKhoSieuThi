    public void build() {
        DanhSachXuatKho = DuLieuMau;
        this.count = this.DanhSachXuatKho.size();
        jLabelKetQua.setText("Có tổng cộng " + count + " kết quả");
        if (count % 20 == 0) {
            SoTrang = count / 20;
        } else {
            SoTrang = count / 20 + 1;
        }
        jLabelSoTrang.setText("1/" + SoTrang);
        jLabelTrang.setText("1");
        ArrayList<ThongTinNhap> table = busNhapKho.getInstance().get20NhapKho(DanhSachXuatKho, 1);
        listDanhSachNhapKho(table);
        NhanVienDangNhap();

    }

    public void listDanhSachNhapKho(ArrayList<ThongTinNhap> arr) {
        DefaultTableModel model = (DefaultTableModel) jTableNhapKho.getModel();
        while (jTableNhapKho.getRowCount() > 0) {
            model.removeRow(0);
        }
        for (int i = arr.size() - 1; i > 0; i--) {
            ThongTinNhap item = arr.get(i);
            model.addRow(new Object[]{item.thoi_gian, item.so_tien_lo + " VNĐ", item.so_tien_sp + " VNĐ", item.ten_nv});
        }
    }

    public void NhanVienDangNhap() {
        if (id_nv != 0) {
            TaiKhoan tk = DAO.daoTaiKhoan.getInstance().getTaiKhoan(id_nv);
            NhanVien nv = DAO.daoTaiKhoan.getInstance().getNhanVien(tk.id_nv);
            jComboBoxNhanVien.addItem(nv.ten_nv);
            jComboBoxNhanVien.addItem("Thông tin");
            jComboBoxNhanVien.addItem("Thoát");
        } else {
            jComboBoxNhanVien.addItem("Chưa đăng nhập");
        }
    }

